function v = func2(X)

y = zeros(1,size(X)(2));

a = [0,0,900699,-0.284418,0.043813,0.005107.-0.005625,0.002085,-0.000546,0.000115,-0.000020,0.000003];
x = [0,0.6,1.2,1.8,2.4,3.0,3.6,4.2,4.8,5.4,6.0];
mul = ones(1,size(X)(2));
for i = 1:11
y = y .+ (a(i)*mul);
mul = mul .* (X .- x(i));
end


plot(X,y);
end